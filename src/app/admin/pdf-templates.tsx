// Tab button
function TabButton({
  label,
  count,
  isActive,
  onPress,
}: {
  label: string;
  count?: number;
  isActive: boolean;
  onPress: () => void;
}) {
  return (
    <Pressable
      onPress={onPress}
      className={cn(
        'flex-row items-center rounded-lg px-4 py-2',
        isActive ? 'bg-violet-500' : 'bg-slate-800'
      )}
    >
      <Text className={cn('text-sm font-medium', isActive ? 'text-white' : 'text-slate-400')}>
        {label}
      </Text>
      {count !== undefined && (
        <View className={cn('ml-2 rounded-full px-1.5 py-0.5', isActive ? 'bg-white/20' : 'bg-slate-700')}>
          <Text className={cn('text-xs', isActive ? 'text-white' : 'text-slate-500')}>{count}</Text>
        </View>
      )}
    </Pressable>
  );
}

