// Status badge
function StatusBadge({ status }: { status: PDFTemplateStatus }) {
  const config = {
    active: { bg: 'bg-emerald-500/20', text: 'text-emerald-400', label: 'Active' },
    draft: { bg: 'bg-amber-500/20', text: 'text-amber-400', label: 'Draft' },
    archived: { bg: 'bg-slate-500/20', text: 'text-slate-400', label: 'Archived' },
  };

  const { bg, text, label } = config[status];

  return (
    <View className={cn('rounded-full px-2.5 py-1', bg)}>
      <Text className={cn('text-xs font-medium', text)}>{label}</Text>
    </View>
  );
}

// Type badge
function TypeBadge({ type }: { type: PDFTemplateType }) {
  const config = {
    daily: { bg: 'bg-cyan-500/20', text: 'text-cyan-400', label: 'Daily' },
    weekly: { bg: 'bg-violet-500/20', text: 'text-violet-400', label: 'Weekly' },
  };

  const { bg, text, label } = config[type];

  return (
    <View className={cn('rounded px-2 py-0.5', bg)}>
      <Text className={cn('text-xs', text)}>{label}</Text>
    </View>
  );
}

